package edu.mysore.asas.aura_hospital.models;

public class HospitalModel {
    public String hosid;
    public String hosname;
    public String anesthos;
    public String ct;
    public String ents;
    public String cardios;
    public String ervalue;
    public Double latitude;
    public Double longitude;
    public String mri;
    public String pathos;
    public String xray;

    public HospitalModel() {}

    public String getHosid() {
        return hosid;
    }

    public void setHosid(String hosid) {
        this.hosid = hosid;
    }

    public String getHosname() {
        return hosname;
    }

    public void setHosname(String hosname) {
        this.hosname = hosname;
    }

    public String getAnesthos() {
        return anesthos;
    }

    public void setAnesthos(String anesthos) {
        this.anesthos = anesthos;
    }

    public String getCt() {
        return ct;
    }

    public void setCt(String ct) {
        this.ct = ct;
    }

    public String getEnts() {
        return ents;
    }

    public void setEnts(String ents) {
        this.ents = ents;
    }

    public String getCardios() {
        return cardios;
    }

    public void setCardios(String cardios) {
        this.cardios = cardios;
    }

    public String getErvalue() {
        return ervalue;
    }

    public void setErvalue(String ervalue) {
        this.ervalue = ervalue;
    }

    public Double getLatitude() {
        return latitude;
    }

    public void setLatitude(Double latitude) {
        this.latitude = latitude;
    }

    public Double getLongitude() {
        return longitude;
    }

    public void setLongitude(Double longitude) {
        this.longitude = longitude;
    }

    public String getMri() {
        return mri;
    }

    public void setMri(String mri) {
        this.mri = mri;
    }

    public String getPathos() {
        return pathos;
    }

    public void setPathos(String pathos) {
        this.pathos = pathos;
    }

    public String getXray() {
        return xray;
    }

    public void setXray(String xray) {
        this.xray = xray;
    }
}
